     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cliente = new Cliente();

        $cliente->nome= $request->txNome;
        $cliente->email= $request->txEmail;
        $cliente->endereco= $request->txEndereco;
        $cliente->telefone= $request->txTelefone;
        $cliente->datacadastro= $request->datacadastro;
        $cliente->sexo= $request->txSexo;
        $cliente->datanascimento= $request->txdatanascimento;
        
        $cliente ->save();

        return redirect('/cliente');

    }

